<h2>Backup/Restaurar</h2>

<p><b>Nota: </b>Backup significa copia de seguridad de toda la informaci&oacute;n del sistema.</p>

<ul>
    <li><b>Bot&oacute;n Nuevo Backup: </b>Genera un backup, y lo guarda en la nube. Para realizar esta acci&oacute;n es necesario tener una cuenta en <a href="https://www.dropbox.com/" target="_blank">Dropbox</a> y tener instalada su aplicaci&oacute;n de escritorio </li><br>
    <li><b>Bot&oacute;n Restaurar Backup: </b>
        Bot&oacute;n peque&ntilde;o de color verde que restaura el backup seleccionado.
        Para restaurar es necesario ingresar la contase&ntilde;a del administrador con la sesi&oacute;n iniciada.<br>
        <b>Nota: </b>Una ves restaurado el backup se cerrara la sesi&oacute;n actual.
    </li>
</ul>
