<h2>Registro de Autores</h2>
<ol>
    <li>
        <h3>Campos</h3>
        <ul>
            <li><b>C&oacute;digo: </b>Este campo muestra el codigo del autor que se registrar&aacute;.</li><br>
            <li><b>Nombre: </b>En este campo se escribe el nombre del autor a registrar.</li><br>
            <li><b>Apellido: </b>Aqu&iacute; se debe escribir el apellido del autor a registrar.
            </li><br>
            <li><b>Fecha de Nacimiento: </b>Despliega un calendario donde se elige la fecha de nacimiento del autor.
            </li><br>
            <li><b>Biograf&iacute;a: </b>Pide seleccionar un archivo pdf que contenga la biograf&iacute;a del autor a registrar.</li><br>
           
        </ul>
    </li>
    <li>
        <h3>Alertas y Mensajes</h3>
        <ul>
            <li><b>Vacio:</b><img src="imagenes/autorVacio.PNG" style="width: 100%;">
            Indica que el campo esta vacio.
            </li><br>
            <li><b>Longitud:</b><img src="imagenes/autorLon.PNG" style="width: 100%;">
            Indica que el valor ingresado en el campo debe ser mas largo.
            </li><br>
            <li><b>Correcto:</b><img src="imagenes/autorCorrecto.PNG" style="width: 100%;">
            Indica que el valor ingresado en el campo cumple las condiciones exigidas.
            </li><br>
            <li><b>Exito:</b><img src="imagenes/autorExito.PNG" style="width: 100%;">
            Indica que autor fue registrado de manera exitosa.
            </li><br>
        </ul>
    </li>
    <li>
        <h3>Botones</h3>
        <ul>
            <li><b>Guardar: </b>Guarda el autor siempre y cuando los campos cumplan las condiciones establecidas.</li><br>
            <li><b>Cancelar: </b>Limpia los campos del formulario</li><br>
            <li><b>OK: </b>Cierra el mensaje de exito y limpia el formulario sin recargar la p&aacute;gina</li><br>
        </ul>
    </li>
</ol>