<h2>Prestamo de Activos</h2>

<ol>
    <li>
         <h3>Datos mostrados en la tabla</h3>
         <img src="imagenes/filaPrestamoActivo.PNG" style="width: 100%;">
        <ul>
           
            
            <li><b>C&oacute;digo: </b>C&oacute;digo del pr&eacute;stamo. </li><br>
            
            <li><b>Nombre: </b>Nombre del usuario que realiz&oacute; el pr&eacute;stamo. </li><br>
            <li><b>Etiqueta de activos Prestados: </b>Aparece al posicionar el puntero sobre una fila
                y muestra la lista de activos incluidos en ese pr&eacute;stamo. </li><br>
            
            <li><b>Fecha de salida: </b>Fecha en que se realiz&oacute; el pr&eacute;stamo. </li><br>
            <li><b>Fecha de devoluci&oacute;n: </b>Fecha acordada entre el administrador y el usuario en la que sedevolveran los libros prestados. </li>
            <li><b>Estado: </b>Mensaje que indica el estado del prestamo. Si esta en rojo significa que la fecha de devolucion ya paso, 
                y si esta en amarillo es porque la fecha de devolucion todavia no a pasado.<br>
                Al dar click sobre este mensaje se puede acceder a la ventana de Actualizar Prestamo </li><br>
        </ul>
    </li>
    
    <li>
         <h3>Ventana de Registro de Pr&acute;stamo de Libros</h3>
         <img src="imagenes/ventanaPrestamoActivo.PNG" style="width: 100%;">
        
        <ul>
            <li><b>Panel de selecci&oacute;n de activos: </b>Permite elegir los activos a prestar, ya sea a traves del codigo de barras de cada uno, o 
                seleccionando la categoria y escribiendo el rango (#-#) de los correlativos que se prestaran.</li>
            
            <li><b>Panel de selecci&oacute;n de usuario: </b>Permite elegir el usuario que realizar&aacute; el pr&eacute;stamo mediante el lector de codigo de barras.<br>
                <b>Nota: </b>Si no posee lector, puede escribir el c&oacute;digo y luego presionar enter para grabar el registro</li>
            <li><b>Fecha de salida: </b>Muestra la fecha actual que es en la que se esta realizando el pr&eacute;stamo.</li>
            <li><b>Campo fecha de devoluci&oacute;n: </b>Campo donde se puede elegir la fecha que se devolver&aacute;n los libros, la cual debe ser acordada entre el usuario y el administrador.</li>
            <li><b>Etiqueta de observaciones: </b>Aparece al posicionar el puntero sobre la foto del usuario
                y muestra las observaciones que el usuario a tenido en pr&eacute;stamos anteriores. </li><br>
        </ul>
    </li>
    
    <li>
       <h3>Ventana de Finalizacion de Pr&acute;stamo</h3>
       <img src="imagenes/ventanaFinalPresActivo.PNG" style="width: 100%;">
        <ul>
            <li><b>Lista: </b>Listado donde aparecen el o los activos incluidos en el pr&eacute;stamo seleccionado.</li>
            <li><b>Selector de Acciones: </b>Puede elegir un nuevo estado para cada activo, seg&uacute; lo haya devuelto el usuario, o poner un estado general para todos.</li>
            <li><b>Campo observaci&oacute;n: </b>Permite escribir una observacion sobre el activo devuelto, la cual sera mostrada la proxima vez que el usuario quiera hacer un prestamo.</li>
            <li><b>Campo fecha de devoluci&oacute;n: </b>Elige la nueva fecha por si el usuario desea extender el tiempo del pr&eacute;stamo.</li>
            <li><b>Bot&oacute;n actualizar: </b>Actualiza la fecha de devoluci&oacute;n segun la fecha elegida en el campo anterior.</li>
            <li><b>Bot&oacute;n Finalizar: </b>Hace la devoluci&oacute;n de todos los activos, de la lista, lo que da por finalizado el pr&eacute;stamo.</li>
            
        </ul>
    </li>
</ol>

