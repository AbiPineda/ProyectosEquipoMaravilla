<h2>Registro de Usuarios</h2>
<ol>
    <li>
        <h3>Campos</h3>
        <ul>
            <li><b>Nombre: </b>Campo donde debe ingresar el nombre del usuario.</li><br>
            <li><b>Apellido: </b>Campo donde se ingresa el apellido del usuario.</li><br>
            <li><b>Direcci&oacute;n </b>Aqu&iacute; se debe escribir la direcci&oacute;n completa del usuario.
            </li><br>
            <li><b>Email: </b>Compo en el que se escribe el correo electronico del usuario.</li><br>
            <li><b>Tel&eacute;fono: </b>Aqu&iacute; se debe escribir el telefono, el cu&aacute;l tiene un formato (####-####) espec&iacute;fico, del usuario a registrar.</li><br>
            <li><b>Instituci&oacute;n: </b>Campo de selecci&oacute;n &uacute;nico donde se elige la institucion en la que estudia el ususario</li><br>
            <li><b>Sexi: </b>Elegir entre las opciones de masculino y femenino.
            </li><br>
            <li><b>Foto: </b>Pide seleccionar la foto del usurio a registrar para agregar al carnet.</li><br>
            <li><b>Nueva Institucion: </b>Ubicada en una ventana emergente que se abre al presionar el bot&oacute;n "+" junto al campo de Instituci&oacute;n</li>
        </ul>
    </li>
    <li>
        <h3>Alertas y Mensajes</h3>
        <ul>
            <li><b>Vacio:</b><img src="imagenes/campoUsuarioVacio.PNG" style="width: 100%;">
            Indica que el campo esta vacio.
            </li><br>
            <li><b>Longitud:</b><img src="imagenes/campoUsuarioLon.PNG" style="width: 100%;">
            Indica que el valor ingresado en el campo debe ser mas largo.
            </li><br>
            <li><b>Correcto:</b><img src="imagenes/campoUsuarioCorrecto.PNG" style="width: 100%;">
            Indica que el valor ingresado en el campo cumple las condiciones exigidas.
            </li><br>
            <li><b>Exito:</b><img src="imagenes/mensajeExitoUsuario.PNG" style="width: 100%;">
            Indica que el usuario fu&eacute; registrado de manera exitosa.
            </li><br>
        </ul>
    </li>
    <li>
        <h3>Botones</h3>
        <ul>
            <li><b>Guardar: </b>Guarda el usuario siempre y cuando los campos cumplan las condiciones establecidas.</li><br>
            <li><b>Cancelar: </b>Limpia los campos del formulario</li><br>
            <li><b>Si, imprimir: </b>Abre en una nueva pesta&ntilde;a un reporte con el carnet  del usuario
                que se acaba de registrar </li><br>
            <li><b>No, salir: </b>Cierra el mensaje de exito y redirecciona a la pagina de inicio de usuarios</li><br>
        </ul>
    </li>
</ol>