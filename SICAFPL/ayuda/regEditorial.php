<h2>Registro de Editoriales</h2>
<ol>
    <li>
        <h3>Campos</h3>
        <ul>
            <li><b>C&oacute;digo: </b>Este campo muestra el codigo de la editorial que se registrar&aacute;.</li><br>
            <li><b>Nombre: </b>En este campo se escribe el nombre de la editorial a registrar.</li><br>
            <li><b>Tel&eacute;fono: </b>Aqu&iacute; se debe escribir el telefono, el cu&aacute;l tiene un formato (####-####) espec&iacute;fico, de la editorial a registrar.
            </li><br>
            <li><b>Email: </b>Solicita ingresar un correo electr&oacute;nico (ejemplo@correo.com) de la editorial que se registrar&aacute;.
            </li><br>
            <li><b>Direcci&oacute;n: </b>Aqu&iacute; se ingresa la direccion de la editorial, la cual debe tener una longitud de al menos 10 caracteres.</li><br>
           
        </ul>
    </li>
    <li>
        <h3>Alertas y Mensajes</h3>
        <ul>
            <li><b>Vacio:</b><img src="imagenes/editorialVacio.PNG" style="width: 100%;">
            Indica que el campo esta vacio.
            </li><br>
            <li><b>Longitud:</b><img src="imagenes/editorialLon.PNG" style="width: 100%;">
            Indica que el valor ingresado en el campo debe ser mas largo.
            </li><br>
            <li><b>Correcto:</b><img src="imagenes/editorialCorrecto.PNG" style="width: 100%;">
            Indica que el valor ingresado en el campo cumple las condiciones exigidas.
            </li><br>
            <li><b>Exito:</b><img src="imagenes/editorialExito.PNG" style="width: 100%;">
            Indica que la editorial fue registrada de manera exitosa.
            </li><br>
        </ul>
    </li>
    <li>
        <h3>Botones</h3>
        <ul>
            <li><b>Guardar: </b>Guarda la editorial siempre y cuando los campos cumplan las condiciones establecidas.</li><br>
            <li><b>Cancelar: </b>Limpia los campos del formulario</li><br>
            <li><b>OK: </b>Cierra el mensaje de exito y limpia el formulario sin recargar la p&aacute;gina</li><br>
        </ul>
    </li>
</ol>