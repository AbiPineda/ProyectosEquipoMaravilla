<title>Ayuda de Registro de Administradores</title>
<h2>Registro de Administradores</h2>
<ol>
    <li>
        <h3>Campos</h3>
        <ul>
            <li><b>Nombre: </b>Campo donde debe ingresar el nombre del administrador.</li><br>
            <li><b>Apellido: </b>Campo donde se ingresa el apellido del administrador.</li><br>
            <li><b>Nombre de Usuario: </b>Aqu&iacute; se debe escribir un  nombre unico con el que iniciar&aacute; sesi&oacute;n.
            </li><br>
            
            <li><b>Dui: </b>Aqu&iacute; se debe escribir dui, el cu&aacute;l tiene un formato (########-#) espec&iacute;fico, del usuario a registrar.</li><br>
            <li><b>Contrase&ntilde;a: </b>Campo donde se debe escribir la contras&ntilde;a con la cual el usuario iniciar&aacute; sesi&oacute;n, debe tener al menso 6 caracteres</li><br>
            <li><b>Repetir Contrase&ntilde;a: </b>Campo donde se debe repetir la contrase&ntilde;a ingresada en el campo anterior.</li><br>
            <li><b>Fecha de Nacimiento: </b>Abre el calendario y debe elegir la fecha de nacimiento del nuevo administrador</li><br>
            <li><b>Email: </b>Compo en el que se escribe el correo electronico del nuevo administrador.</li><br>
            <li><b>Sexo: </b>Elegir entre las opciones de masculino y femenino.
            </li><br>
            
            <li><b>Nivel: </b>Elige entre:
                <ul>
                    <li><b>Administrador: </b>Tiene permiso para entrar en los m&oacute;dulos de Activo Fijo, Biblioteca y Usuario, adem&aacute;s de ver sus datos y modificarlos</li>
                    <li><b>Root: </b>Tiene los mismos permisos del Administrador, pero adem&aacute;s tiene acceso al m&oacute;dulo de seguridad</li>
                </ul>
            </li>
            <li><b>Foto: </b>Pide seleccionar la foto del administrador a registrar.</li><br>
        </ul>
    </li>
    <li>
        <h3>Alertas y Mensajes</h3>
        <ul>
            <li><b>Vacio:</b><img src="imagenes/adminVacio.PNG" style="width: 100%;">
            Indica que el campo esta vacio.
            </li><br>
            <li><b>Longitud:</b><img src="imagenes/adminLon.PNG" style="width: 100%;">
            Indica que el valor ingresado en el campo debe ser mas largo.
            </li><br>
            <li><b>Correcto:</b><img src="imagenes/adminCorrecto.PNG" style="width: 100%;">
            Indica que el valor ingresado en el campo cumple las condiciones exigidas.
            </li><br>
            <li><b>Exito:</b><img src="imagenes/mensajeExitoAdmin.PNG" style="width: 100%;">
            Indica que el administrador fu&eacute; registrado de manera exitosa.
            </li><br>
        </ul>
    </li>
    <li>
        <h3>Botones</h3>
        <ul>
            <li><b>Guardar: </b>Guarda el usuario siempre y cuando los campos cumplan las condiciones establecidas.</li><br>
            <li><b>Cancelar: </b>Limpia los campos del formulario</li><br>
            <li><b>Ok: </b>Cierra el mensaje de exito y redirecciona a la lista de administradores</li><br>
           
        </ul>
    </li>
</ol>